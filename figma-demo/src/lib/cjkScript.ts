const HANS_MARKERS = new Set<string>();
const HANT_MARKERS = new Set<string>();
for (const [hans, hant] of PAIRS) {
  HANS_MARKERS.add(hans);
  HANT_MARKERS.add(hant);
}

function countCjkChars(text: string): number {
  let n = 0;
  for (const ch of text) {
    const cp = ch.codePointAt(0)!;
    if (
      (cp >= 0x4e00 && cp <= 0x9fff) ||
      (cp >= 0x3400 && cp <= 0x4dbf) ||
      (cp >= 0xf900 && cp <= 0xfaff)
    ) {
      n += 1;
    }
  }
  return n;
}

export function detectCjkScript(text: string): CjkScript | null {
  const sample = `${text || ""}`.trim();
  if (!sample || countCjkChars(sample) === 0) return null;

  let hansScore = 0;
  let hantScore = 0;

  for (const ch of sample) {
    if (HANS_MARKERS.has(ch)) hansScore += 1;
    if (HANT_MARKERS.has(ch)) hantScore += 1;
  }

  if (hansScore === 0 && hantScore === 0) return null;
  if (hansScore > hantScore) return "hans";
  if (hantScore > hansScore) return "hant";
  return null;
}

export function cjkScriptFontClass(script: CjkScript | null): string {
  if (script === "hans") return "font-cjk-hans";
  if (script === "hant") return "font-cjk-hant";
  return "font-cjk";
}
